def div1(x,y):
    print('%s/%s = %s'%(x, y, x/y)) #在python3中 / 表示浮点型的除法;在python2 中/ 就是整除除法

def div2(x,y):
    print('%s//%s = %s' %(x,y,x//y)) #//整除

div1(5,2)
div1(5.,2)
div2(5,2)
div2(5.,2.)