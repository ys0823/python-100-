#对100以内的两位数，请使用一个两重循环打印出所有十位数数字比个位数数字小的数，
# 例如，23（2 < 3），并统计个数
#2018-8-19
#----------------------------------------------
num = 0
for i in range(10,100):
    a = i / 10
    b = i % 10
    if a < b:
        print(i)
        num +=1
print("总共有%d个数"%num)

