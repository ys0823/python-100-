D = input("请输入一个星期日期:\n")
if D[0]=='M':
    print("Monday")
if D[0] =='W':
    print("Wednesday")
if D[0] =='T':
    if D[1] =='u':
        print("Tuesday")
    if D[1] =='h':
        print("Thursday")
if D[0] =='S':
    if D[1] =='a':
        print('Saturday')
    if D[1] =='u':
        print('Sunday')
