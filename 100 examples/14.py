n = num =int(input('please input a number:\n'))

f=[]
for j in range(int(num/2)+1): #控制除的次数
    for i in range(2,n):  #控制除的因数
        t = n%i
        if t==0:
            f.append(i)
            n=n//i
            break
if len(f) ==0:
    print('该数字没有质因数')
else:
    f.append(n)
    f.sort()
    print('%d=%d'%(num, f[0]),end='')#这里不换行
    for i in range(1,len(f)):
        print('%d'%f[i],end='')