#问题描述：
#已知有五位朋友在一起。第五位朋友他说自己比第4个人大2岁；
#问第4个人岁数，他说比第3个人大2岁；
#问第三个人，又说比第2人大两岁；
#问第2个人，说比第一个人大两岁；
#最后问第一个人，他说是10岁
#要求：求第5个人的年龄是多少。
#2018-8-9
#---------------------------------
def age(n):
    if n ==1:
        c = 10
    else:
        c = age(n-1)+2
    return c
print(age(5))