#Python逆向打印获取到的正数，并输出的练习。
#Python练习题问题如下：
#raw_input获取给定的一个不多于5位的正整数。
#要求有二：
#一、求它是几位数；
#二、逆序打印出各位数字。
#2018-8-9
#------------------------------------------
n = input("please input a number between 0~99999:\n")
m = len(n)
for i in range(m-1,-1,-1): #索引范围为(m-1)~0
    print(n[i],end='')

