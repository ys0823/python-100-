#公鸡5文钱一只，母鸡3文钱一只，小鸡3只一文钱，用100文钱买一百只鸡,
#其中公鸡，母鸡，小鸡都必须要有，问公鸡，母鸡，小鸡要买多少只刚好凑足100文钱。
#2018-8-13
#---------------------------------------
for i in range(1,21):
    for j in range(1,34):
        for k in range(1,33):
            m = 5*i+3*j+k
            n = i+j+3*k
            if m==100 and n==100:
                print("公鸡%d只,母鸡%d只,小鸡%d只"%(i,j,k*3))
